// Ex 9.8: Quadrilaterals and inheritance. I use the following.
//		   https://en.wikipedia.org/wiki/Quadrilateral#Taxonomy

public class Point {
	private final int x;
	private final int y;

	public int getX() { return x; }
	public int getY() { return y; }

	public Point(int x, int y) { this.x = x; this.y = y; }
}
